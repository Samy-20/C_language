#include <stdio.h>

int main()
{
    int a = 4;
    float b = 9.5;
    char c = 'a';
    int d = 35;
    int e = 35 + 4;
    printf("The value of a is %d \n", a);
    printf("The value of b is %0.3f \n", b);
    printf("The value of c is %c \n", c);
    printf("Sum of a and d is %d \n", a - d);
    printf("Sum of a and d is %d \n", e);
    return 0;
}

